function ouvrirModaleConnexion() {
    const modale = document.querySelector("#modal-connexion");
    modale.style.display = "block";
    console.log(modale); 
  }


function fermerModaleConnexion() {
  const modale = document.querySelector("#modal-connexion");
  modale.style.display = "none";
  console.log(modale);
}
